#include <iostream>

int main()
{
    std::cout << "8/5 = " << 8/5 << "*5 + " << 8%5 << '\n';
    std::cout << "-8/5 = " << -8/5 << "*5 + " << -8%5 << '\n';
    std::cout << "8/-5 = " << 8/-5 << "*5 + " << 8%-5 << '\n';
    std::cout << "8.0 / 0.0 = " << 8.0/0.0 << '\n';
    std::cout << "8 / 0 = " << 8 / 0 << '\n';
    return 0;
}
